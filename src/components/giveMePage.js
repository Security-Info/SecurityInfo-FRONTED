function changePage (nextPage)
{
    if (nextPage === "home") {
        window.location = "Home.html";
    }
    else if (nextPage === "login") {
        window.location = "Login.html";
    }
    else if (nextPage === "map") {
        window.location = "Map.html";
    }
    else if (nextPage === "contact") {
        window.location = "Contact.html";
    }
    else if (nextPage === "contactUser") {
        window.location = "ContactUser.html";
    }
    else if (nextPage === "profile") {
        window.location = "Profile.html";
    }
    else if (nextPage === "about") {
        window.location = "About.html";
    }
    else if (nextPage === "aboutUser") {
        window.location = "AboutUser.html";
    }
    else if (nextPage === "report") {
        window.location = "Report.html";
    }
    else if (nextPage === "register") {
        window.location = "Register.html";
    }
    else if (nextPage === "fiscalia") {
        window.location = "https://www.fiscalia.gov.co/colombia/servicios-de-informacion-al-ciudadano/donde-y-como-denunciar/";
    }
    else if (nextPage === "policia") {
        window.location = "https://adenunciar.policia.gov.co/Adenunciar/Login.aspx?ReturnUrl=%2fadenunciar%2f";
    }
}